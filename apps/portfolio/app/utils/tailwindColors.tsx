export type tailwindBaseColor = keyof typeof tailwindColors;
export type tailwindShades = keyof (typeof tailwindColors)[tailwindBaseColor];
function getTailwindHexFromClassName(classname: string) {
  const [color, shade] = classname.split("-");
  const shadeKey = parseInt(shade) as tailwindShades;

  return tailwindColors[color as tailwindBaseColor][shadeKey];
}

export function getRandomBaseColor(): tailwindBaseColor {
  const baseColors = Object.keys(tailwindColors);
  const randomIndex = Math.floor(Math.random() * baseColors.length);
  return baseColors[randomIndex] as tailwindBaseColor;
}

export default getTailwindHexFromClassName;
